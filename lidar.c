//***********Function for reading data from Sensor-1***************
void read_data_uart1(){
	memset( usart_rx_buf, 0, sizeof( usart_rx_buf)); // Clear the data buffer.
	HAL_UART_Receive(&huart1, usart_rx_buf, 9, HAL_MAX_DELAY);
	uint16_t dist, strength=0;
	uint8_t checksum, chip_temp=0;
	if (usart_rx_buf[0]==HEADER && usart_rx_buf[1]==HEADER){
		for (int i=0;i<8;i++){
			checksum += usart_rx_buf[i];
		}
		if (usart_rx_buf[8] == (checksum&0xff)){
			dist = usart_rx_buf[2] | (usart_rx_buf[3]<<8);
			strength = usart_rx_buf[4] |(usart_rx_buf[5]<<8);
			chip_temp = usart_rx_buf[6] | (usart_rx_buf[7]<<8);
      chip_temp = chip_temp/8 - 256;
			//printf("Distance: %d cm \t", dist);
      //printf("Strength: %d \t", strength);
      //printf("Chip temp: %d degree celcius\t\n", chip_temp); 
			printf("LiDAR-1: dist = %d cm; strength = %d; temp = %d.\r\n", dist,strength,chip_temp); // Print everything in single line.
			//HAL_UART_Transmit(&huart2,usart_rx_buf,sizeof(usart_rx_buf), HAL_MAX_DELAY); // Print full Data packet in HEX format.
			
			for(int j=0; j<1000; j++){}; // This will create a short delay.
			memset( usart_rx_buf, 0, sizeof( usart_rx_buf)); // Clear the data buffer.   
		}

	}
	
	
}
